#ifndef PARAMETERTYPES_H
#define PARAMETERTYPES_H

#include <QString>

/**
 * @brief Перечисление типов параметров, используемых в системе ГАЛС.
 *
 * Данное перечисление служит для идентификации параметров различных устройств
 * (гиперспектрометр, обзорная камера, датчик освещённости, эксперимент)
 * при передаче их изменений между GUI, фасадом и аппаратными модулями.
 *
 * Используется в сигналах и слотах для унифицированной обработки изменений
 * параметров и обратной связи.
 */
enum class ParameterType {
    /**
     * @brief Экспозиция гиперспектрометра.
     *
     * Единица измерения: миллисекунды (мс).
     * Диапазон: зависит от камеры, обычно 1–2000 мс.
     */
    HS_EXPOSURE,

    /**
     * @brief Частота кадров гиперспектрометра.
     *
     * Единица измерения: кадры в секунду (fps).
     * Диапазон: 1–60 fps.
     */
    HS_FRAMERATE,

    /**
     * @brief Экспозиция обзорной камеры.
     *
     * Единица измерения: миллисекунды (мс).
     * Диапазон: 0–1000 мс.
     */
    OC_EXPOSURE,

    /**
     * @brief Частота кадров обзорной камеры.
     *
     * Единица измерения: кадры в секунду (fps).
     * Диапазон: 1–60 fps.
     */
    OC_FRAMERATE,

    /**
     * @brief Экспозиция датчика освещённости.
     *
     * Единица измерения: миллисекунды (мс).
     * Диапазон: 1–1000 мс.
     */
    LIGHT_EXPOSURE,

    /**
     * @brief Частота опроса датчика освещённости.
     *
     * Единица измерения: кадры в секунду (fps).
     * Диапазон: 1–60 fps.
     */
    LIGHT_FRAMERATE,

    /**
     * @brief Текущая высота измерений (используется для расчёта GSD и FOV).
     *
     * Единица измерения: метры (м).
     * Диапазон: 2–1000 м.
     */
    EXP_ALTITUDE,

    /**
     * @brief Высота начала автоматической записи.
     *
     * При достижении этой высоты (при наборе) запускается регистрация данных.
     * При снижении ниже – останавливается.
     *
     * Единица измерения: метры (м).
     * Диапазон: 2–1000 м.
     */
    EXP_RECORD_START_ALTITUDE,

    /**
     * @brief Значение для пустой инициализации
     */
    NO_PARAMETER
};

/**
 * @brief Структура для хранения всех параметров, необходимых для создания
 * виджета ParameterModificator.
 *
 * Позволяет компактно передавать в метод addParameter полный набор настроек:
 * - имя и единицу измерения;
 * - диапазон допустимых значений;
 * - начальное значение;
 * - три шага изменения (малый, средний, большой).
 *
 * Использование этой структуры упрощает добавление параметров из
 * конфигурационных файлов, массивов или при массовом создании виджетов.
 */
struct ParameterInfo {
    ParameterType type;  //!< Тип параметра
    QString name;  //!< Отображаемое имя параметра.
    QString unit;  //!< Единица измерения (например, "мс", "fps").
    int minVal;  //!< Минимальное допустимое значение.
    int maxVal;  //!< Максимальное допустимое значение.
    int initialValue;  //!< Начальное значение.
    int step1;         //!< Первый шаг изменения (малый).
    int step2;  //!< Второй шаг изменения (средний).
    int step3;  //!< Третий шаг изменения (большой).

    /**
     * @brief Конструктор для удобного создания структуры.
     * @param type_ Имя параметра.
     * @param name_ Имя параметра.
     * @param unit_ Единица измерения.
     * @param min_ Минимум.
     * @param max_ Максимум.
     * @param initialValue_ Начальное значение.
     * @param step1_ Малый шаг.
     * @param step2_ Средний шаг.
     * @param step3_ Большой шаг.
     *
     * Все параметры имеют значения по умолчанию, что позволяет создавать
     * структуру с частичной инициализацией.
     */
    ParameterInfo(ParameterType type_ = ParameterType::NO_PARAMETER,
                  const QString &name_ = QString(),
                  const QString &unit_ = QString(), int min_ = 0,
                  int max_ = 100, int initialValue_ = 0, int step1_ = 1,
                  int step2_ = 10, int step3_ = 100)
        : type(type_),
          name(name_),
          unit(unit_),
          minVal(min_),
          maxVal(max_),
          initialValue(initialValue_),
          step1(step1_),
          step2(step2_),
          step3(step3_) {}
};
#endif  // PARAMETERTYPES_H
